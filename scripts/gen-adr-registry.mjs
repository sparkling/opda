#!/usr/bin/env node
/**
 * Generate src/lib/adr-pages.mjs — the ADR page registry — from the committed
 * MADR corpus in docs/adr/ADR-*.md.
 *
 * Mirrors the ODR surfacing pattern (src/lib/odr-pages.mjs), but ADRs are
 * MADR-format (no `kind` facet), so the registry carries number / title /
 * status / date only. The registry is the single source of truth shared by:
 *   - src/pages/modelling/adr/index.astro  (listing)
 *   - src/pages/modelling/adr/[id].astro   (detail-page meta pill + breadcrumb)
 *   - src/lib/site.ts                       (left-nav group, generated from it)
 *
 * Each ADR's body renders through the Astro `adr` content collection
 * (render(entry) → <Content/> in [id].astro), the same path as ODRs.
 *
 * Deterministic: re-run after adding/editing an ADR.  `make gen-adr` /
 * `node scripts/gen-adr-registry.mjs`.  Use --check to fail (non-zero) if the
 * committed registry is stale (CI-friendly), without rewriting it.
 */
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const ADR_DIR = join(ROOT, 'docs', 'adr');
const OUT = join(ROOT, 'src', 'lib', 'adr-pages.mjs');

/** Parse the leading `--- ... ---` YAML-ish frontmatter into a flat string map. */
function frontmatter(text) {
  const m = text.match(/^---\n([\s\S]*?)\n---/);
  const out = {};
  if (!m) return out;
  for (const line of m[1].split('\n')) {
    const mm = line.match(/^([\w-]+):\s*(.*)$/);
    if (mm) out[mm[1]] = mm[2].trim();
  }
  return out;
}

/** First `# Heading` line, the MADR title. */
function h1(text) {
  for (const line of text.split('\n')) {
    if (line.startsWith('# ')) return line.slice(2).trim();
  }
  return '';
}

function collect() {
  const files = readdirSync(ADR_DIR)
    .filter((f) => /^ADR-\d{4}[a-z]?-.*\.md$/.test(f))
    .sort();
  return files.map((file) => {
    const text = readFileSync(join(ADR_DIR, file), 'utf8');
    const fm = frontmatter(text);
    const number = file.match(/^ADR-(\d{4}[a-z]?)/)[1];
    return {
      id: `adr-${number}`,
      number,
      title: h1(text),
      status: fm.status || '',
      date: fm.date || '',
    };
  });
}

function render(records) {
  const rows = records
    .map((r) => {
      const id = JSON.stringify(r.id);
      const number = JSON.stringify(r.number);
      const title = JSON.stringify(r.title);
      const status = JSON.stringify(r.status);
      const date = JSON.stringify(r.date);
      return `  { id: ${id}, number: ${number}, title: ${title}, status: ${status}, date: ${date} },`;
    })
    .join('\n');

  return `/**
 * ADR page registry — GENERATED by scripts/gen-adr-registry.mjs from
 * docs/adr/ADR-*.md.  DO NOT EDIT BY HAND; re-run \`make gen-adr\` instead.
 *
 * ADRs are MADR-format (no \`kind\` facet), so each record carries number,
 * title (the MADR H1), status, and date.  Single source of truth shared by:
 *   - src/pages/modelling/adr/index.astro  (listing)
 *   - src/pages/modelling/adr/[id].astro   (detail-page meta pill + breadcrumb)
 *   - src/lib/site.ts                       (left-nav group)
 *
 * Each ADR's body renders through the Astro \`adr\` content collection
 * (render(entry) → <Content/> in [id].astro) — the same pipeline as ODRs.
 */

/**
 * @typedef {Object} AdrEntry
 * @property {string} id      - Kebab-case page id, e.g. 'adr-0001'
 * @property {string} number  - Zero-padded number, e.g. '0001'
 * @property {string} title   - MADR H1 title
 * @property {string} status  - 'accepted' | 'proposed' | 'superseded' | 'rejected' | ''
 * @property {string} date    - ISO date string from frontmatter, or ''
 */

/** All ${records.length} ADRs in the corpus. Every ADR gets a live page. */
export const ADR_REGISTRY = [
${rows}
];
`;
}

const records = collect();
const next = render(records);

if (process.argv.includes('--check')) {
  let current = '';
  try {
    current = readFileSync(OUT, 'utf8');
  } catch {
    /* missing → stale */
  }
  if (current !== next) {
    console.error(
      `[gen-adr-registry] src/lib/adr-pages.mjs is stale (${records.length} ADRs). Run: make gen-adr`,
    );
    process.exit(1);
  }
  console.log(`[gen-adr-registry] OK — registry matches ${records.length} ADRs.`);
} else {
  writeFileSync(OUT, next);
  console.log(`[gen-adr-registry] wrote src/lib/adr-pages.mjs (${records.length} ADRs).`);
}
