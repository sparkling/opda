# --- Graph builder --------------------------------------------------------
def _bind_common(graph: Graph) -> None:
    """Bind the prefixes named in the ADR-0010 §"Emission structure" template.

    The canonical serialiser filters out prefixes whose namespace isn't
    referenced by the graph, so it's safe to bind broadly here.
    """
    graph.bind("opda", OPDA)
    graph.bind("opda-v", OPDA_V)
    graph.bind("skos", SKOS)
    graph.bind("dct", DCTERMS)
    graph.bind("prov", PROV)
    graph.bind("owl", OWL)
    graph.bind("xsd", XSD)


def build_vocabularies_graph() -> Graph:
    """Build the rdflib Graph holding all 16 SKOS Concept Schemes.

    Per ADR-0010 §"Per-scheme metadata MUST-haves":
      - each scheme: rdf:type skos:ConceptScheme + skos:prefLabel @en +
        dct:title @en + skos:definition @en + dct:source + opda:ufoCategory
        + skos:scopeNote @en + opda:hasSteward @en.
    Per ADR-0010 §"Per-member MUST-haves":
      - each member: rdf:type skos:Concept + skos:inScheme + skos:prefLabel
        @en + skos:notation + skos:definition @en + dct:source.
    """
    g = Graph()
    _bind_common(g)

    for scheme in _all_schemes():
        scheme_uri = scheme.scheme_uri()
        g.add((scheme_uri, RDF.type, SKOS.ConceptScheme))
        g.add((scheme_uri, SKOS.prefLabel, Literal(scheme.pref_label, lang="en")))
        g.add((scheme_uri, DCTERMS.title, Literal(scheme.title, lang="en")))
        g.add((scheme_uri, SKOS.definition, Literal(scheme.definition, lang="en")))
        g.add((scheme_uri, DCTERMS.source, scheme.scheme_source))
        g.add((scheme_uri, OPDA.ufoCategory, Literal(scheme.ufo_category)))
        scope_note_text = scheme.scope_note
        if scheme.placeholder_warning:
            scope_note_text = (
                f"{scope_note_text} PLACEHOLDER: {scheme.placeholder_warning}"
            )
        g.add((scheme_uri, SKOS.scopeNote, Literal(scope_note_text, lang="en")))
        g.add((scheme_uri, OPDA.hasSteward, Literal(scheme.steward, lang="en")))

        # Index members by notation so `skos:narrower` (intra-scheme) can be
        # resolved to member URIs (used by opda:PerilScheme sub-perils).
        by_notation = {m.notation: m for m in scheme.members}
        for member in scheme.members:
            member_uri = scheme.member_uri(member)
            g.add((member_uri, RDF.type, SKOS.Concept))
            g.add((member_uri, SKOS.inScheme, scheme_uri))
            g.add((member_uri, SKOS.prefLabel, Literal(member.pref_label, lang="en")))
            g.add((member_uri, SKOS.notation, Literal(member.notation)))
            g.add((member_uri, SKOS.definition, Literal(member.definition, lang="en")))
            g.add((member_uri, DCTERMS.source, member.member_source))
            if member.derived_from is not None:
                g.add((member_uri, PROV.wasDerivedFrom, member.derived_from))
            if member.top_concept_of:
                g.add((member_uri, SKOS.topConceptOf, scheme_uri))
            for narrower_notation in member.narrower:
                narrower_member = by_notation[narrower_notation]
                g.add((
                    member_uri,
                    SKOS.narrower,
                    scheme.member_uri(narrower_member),
                ))

    return g


# --- Generator-comment header --------------------------------------------
def _comment_header(emission_date: str, git_sha: str) -> str:
    """Build the generator-comment block prepended to `opda-vocabularies.ttl`.

    Mirrors the foundation.py header pattern; per ADR-0010 §"Emission
    structure" lines 55-58 the header cites generator version, source
    commit, and ADR backlinks, plus a DO-NOT-HAND-EDIT warning. Two
    extra explanatory lines reference ODR-0011 §1a + §8a so the human
    reader sees the Council contract this file realises.
    """
    lines = [
        f"# {VOCABULARIES_FILENAME} — OPDA SKOS Concept Schemes",
        f"# Generated by opda-gen {__version__} at {emission_date}; "
        f"DO NOT HAND-EDIT.",
        "# Specification: "
        "https://openpropdata.org.uk/adr/ADR-0007-ontology-generator-specification",
        "# Implementation: "
        "https://openpropdata.org.uk/adr/ADR-0008-generator-implementation-infrastructure",
        "# This emission: "
        "https://openpropdata.org.uk/adr/ADR-0010-skos-vocabulary-emission",
        "# Scope expansion (G8/G9/G10): "
        "https://openpropdata.org.uk/adr/ADR-0013-overlay-profile-emission",
        f"# Generator version: opda-gen-{__version__}",
        f"# Source commit: {git_sha}",
        "# Schemes per ODR-0011 §1a + ODR-0011 §8a seven-category UFO framework.",
        "# Per-scheme deprecation lifecycle per ODR-0011 §5a SHACL-AF pattern",
        "# (rule body emitted into opda-shapes.ttl under ADR-0012).",
        "",
    ]
    return "\n".join(lines) + "\n"


# --- Public API ----------------------------------------------------------
def emit_vocabularies(
    output_dir: Path,
    *,
    emission_date: str | None = None,
    git_sha: str | None = None,
) -> dict[Path, str]:
    """Emit `opda-vocabularies.ttl` into ``output_dir``.

    Returns a dict mapping written ``Path`` → Turtle content (utf-8 str),
    matching the foundation emitter's interface for callers (tests, CI)
    that want to inspect emitted content without re-reading from disk.

    ``emission_date`` and ``git_sha`` default to the pinned constants
    (``_VOCABULARIES_LAST_MODIFIED`` / ``_VOCABULARIES_SOURCE_COMMIT``)
    per ADR-0010. They MUST stay pinned for byte-identity CI to pass;
    the constants advance when a future ADR materially mutates the
    vocabulary content. Override them in tests to exercise alternate
    values.
    """
    output_dir.mkdir(parents=True, exist_ok=True)
    date_str = emission_date or _VOCABULARIES_LAST_MODIFIED
    sha_str = git_sha or _VOCABULARIES_SOURCE_COMMIT

    graph = build_vocabularies_graph()
    header = _comment_header(date_str, sha_str)
    body = to_canonical_turtle(graph).decode("utf-8")
    content = header + body

    out_path = output_dir / VOCABULARIES_FILENAME
    out_path.write_text(content, encoding="utf-8", newline="")
    return {out_path: content}


# Backwards-compat alias used by the legacy ADR-0008 CLI stub. The
# `emit-vocabularies` subcommand now dispatches to `emit_vocabularies`;
# the stub `emit` name is kept so anything still importing it sees the
# real implementation instead of the original `NotImplementedError`.
def emit(output_dir: Path) -> dict[Path, str]:  # pragma: no cover - alias
    return emit_vocabularies(output_dir)
